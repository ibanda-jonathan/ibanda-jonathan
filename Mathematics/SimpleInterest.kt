fun main() {
    // Math: I = (P * R * T) / 100
    val principal = 150000.0 // UGX
    val rate = 10.0 // 10%
    val time = 2.0 // 2 Years
    
    val interest = (principal * rate * time) / 100
    
    println("--- S3 Math Project ---")
    println("Principal: UGX $principal")
    println("Interest: UGX $interest")
}
